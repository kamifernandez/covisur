#pragma mark Metodos Check Network Status

-(void) checkNetworkStatus:(NSNotification *)notice{
    // called after network status changes
    NetworkStatus internetStatus = [internetReachable currentReachabilityStatus];
    NSUserDefaults * _defaults = [NSUserDefaults standardUserDefaults];
    switch (internetStatus)
    {
        case NotReachable:
        {
            NSLog(@"The internet is down.");
            if (mostrarAlerta == NO) {
                NSMutableDictionary *msgDict=[[NSMutableDictionary alloc] init];
                [msgDict setValue:@"AtenciÃ³n" forKey:@"Title"];
                [msgDict setValue:@"Actualmente no se encuentra conectado a internet" forKey:@"Message"];
                [msgDict setValue:@"Aceptar" forKey:@"Aceptar"];
                [msgDict setValue:@"100" forKey:@"Tag"];
                
                [self performSelectorOnMainThread:@selector(showAlert:) withObject:msgDict
                                    waitUntilDone:YES];
                mostrarAlerta = YES;
            }
            
            [_defaults setObject:@"NO" forKey:@"connectioninternet"];
            [_defaults synchronize];
            break;
        }
        case ReachableViaWiFi:
        {
            NSLog(@"The internet is working via WIFI.");
            //hayConexionInternet = YES;
            [_defaults setObject:@"YES" forKey:@"connectioninternet"];
            [_defaults synchronize];
            break;
        }
        case ReachableViaWWAN:
        {
            NSLog(@"The internet is working via WWAN.");
            //hayConexionInternet = YES;
            [_defaults setObject:@"YES" forKey:@"connectioninternet"];
            [_defaults synchronize];
            break;
        }
    }
}

#pragma mark - showAlert metodo

-(void)showAlert:(NSMutableDictionary *)msgDict
{
    UIAlertController * alert = [UIAlertController
                                 alertControllerWithTitle:[msgDict objectForKey:@"Title"]
                                 message:[msgDict objectForKey:@"Message"]
                                 preferredStyle:UIAlertControllerStyleAlert];
    
    if ([[msgDict objectForKey:@"Cancel"] length]>0) {
        UIAlertAction* yesButton = [UIAlertAction
                                    actionWithTitle:[msgDict objectForKey:@"Aceptar"]
                                    style:UIAlertActionStyleDefault
                                    handler:^(UIAlertAction * action) {
                                        
                                    }];
        
        UIAlertAction* noButton = [UIAlertAction
                                   actionWithTitle:[msgDict objectForKey:@"Cancel"]
                                   style:UIAlertActionStyleDefault
                                   handler:^(UIAlertAction * action) {
                                       
                                   }];
        
        [alert addAction:yesButton];
        [alert addAction:noButton];
    }else{
        UIAlertAction* yesButton = [UIAlertAction
                                    actionWithTitle:[msgDict objectForKey:@"Aceptar"]
                                    style:UIAlertActionStyleDefault
                                    handler:^(UIAlertAction * action) {
                                        if ([[msgDict objectForKey:@"Tag"] isEqualToString:@"100"]) {
                                            mostrarAlerta = NO;
                                        }
                                    }];
        
        [alert addAction:yesButton];
    }
    
    [self.window.rootViewController presentViewController:alert animated:YES completion:nil];
}


